prenom = "Damien" # prenom est un str (chaine de caractère)
print(prenom)     # affiche "David"
prenom = 2        # prenom devient un int (entier)
print(prenom)     # affiche 2
prenom = 3.5      # prénom devient un float (réel)
print(prenom)     # affiche 3.5

age: int = 33     # age est explicitement déclaré comme un int
print(age)        # affiche 33
age = "coucou"    # Malgré la déclaration explicite, age est maintenant un str
print(age)        # affiche "coucou"